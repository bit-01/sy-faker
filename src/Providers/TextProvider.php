    public function words(int $count = 5, bool $asArray = false, string $separator = ' '): array|string
    {
        $result = [];

        for ($i = 0; $i < $count; $i++) {
            $result[] = $this->words[array_rand($this->words)];
        }

        return $asArray ? $result : implode($separator, $result);
    }


